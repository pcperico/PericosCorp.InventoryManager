     private void fillTable(List<CategoryProductDto> categories,DefaultTableModel model)    
    {        
        model.setRowCount(0);
        this.tbl_categories.setModel(model);             
        this.tbl_categories.getColumnModel().getColumn(3).setMinWidth(0);
        this.tbl_categories.getColumnModel().getColumn(3).setMaxWidth(0);        
        Object [] fila = new Object[4];
        for(CategoryProductDto pc:categories)
        {
           fila[0] = pc.getName();
           fila[1] = pc.getDescription();
           fila[2] = pc.getProductsCount();
           fila[3] = pc.getId();
           model.addRow(fila);
        }            
    }
     
