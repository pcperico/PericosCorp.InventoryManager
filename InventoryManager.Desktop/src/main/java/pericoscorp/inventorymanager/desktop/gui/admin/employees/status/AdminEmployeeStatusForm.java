    private void fillTable(List<EmployeeStatus> statuses, DefaultTableModel model) {
        model.setRowCount(0);
        this.tbl_statuses.setModel(model);
        this.tbl_statuses.getColumnModel().getColumn(1).setMinWidth(0);
        this.tbl_statuses.getColumnModel().getColumn(1).setMaxWidth(0);
        Object[] fila = new Object[2];
        for (EmployeeStatus r : statuses) {
            fila[0] = r.getName();
            fila[1] = r.getId();
            model.addRow(fila);
        }
    }

